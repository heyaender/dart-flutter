/// PrintInteger() takes an integer as an argument and prints it to the console.
/// 
/// Args:
///   aNumber (int): This is the name of the parameter.
void printInteger(int aNumber) {
  print('The number is $aNumber');
}

/// The printInteger() function takes an integer as an argument and prints it to the console.
void main() {
  var number = 77;
  printInteger(number);
}
